package garage;
//the car class represents a car object
public class Car {
	String model;
	String color;
	String modelPackage;
	//the car constructor, which specifies the car object's model, color, and package
	public Car(String model, String color, String modelPackage)
	{
		this.model = model;
		this.color = color;
		this.modelPackage = modelPackage;
	}
}
